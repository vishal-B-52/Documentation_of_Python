list = ["Object", "namer", "Fame", 12, "Goalet", 24, 55, "Amrty"]

list.insert()  # adds the specified object in the desired index. requires two arguments(0, string").
list.pop()  # removed last object in the list.
list.remove()  # removes down specified object.requires one argument.
list.clear()  # clears down whole string.
list.reverse()  # reverses the order of list.
list.extend()  # extends the list1 with list2. This will update list1 with list2 elements.
list.copy()  # copies down whole list.
list.sort()  # sorts and arranges every object in list into ascending order. Gives error if integer and string are
# present in the list.
list.index()  # displays position of the specified string or object.
list.count()  # counts how many times a letter or object has occured in the list.

